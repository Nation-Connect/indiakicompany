                                    <h4>Why Choose Limited Liability Partnership (LLP)</h4><br>
                                    
                                    <ul class="attractive-list">
                                        <li><span class="list-heading"></span> Separate legal entity, akin to companies.</li>
                                        <li><span class="list-heading"> </span> Minimum of two partners needed to establish LLP.</li>
                                        <li><span class="list-heading"></span> No upper limit on partners.</li>
                                        <li><span class="list-heading"> </span>At least two designated partners required.</li>

                                        <li><span class="list-heading"> </span>One designated partner must be an Indian resident.</li>

                                        <li><span class="list-heading"> </span>Limited liability for each partner, based on contributions.</li>

                                        <li><span class="list-heading"> </span>Low formation costs and minimal compliance.</li>

                                        <li><span class="list-heading"> </span> No minimum capital contribution required.</li>
                                    </ul>
                                    <br>
                                    <h4>Advantages of LLP:</h4> <br>
<ul>
    <li><span class="list-heading">Separate Legal Entity:</span> LLP is distinct from partners, gaining stakeholders' trust.</li>
    <li><span class="list-heading">Limited Partner Liability:</span> Partners only liable for their contributions, not personal assets.</li>
    <li><span class="list-heading">Cost-Efficient and Low Compliance:</span> Formation costs and compliance are lower compared to other types of companies.</li>
    <li><span class="list-heading">Flexible Capital Contribution:</span> No minimum capital requirement for incorporation.</li>
</ul><br>
<h4>Disadvantages of LLP:</h4><br>
<ul>
    <li><span class="list-heading">Non-Compliance Penalty:</span> Failing to meet minimal compliance results in heavy penalties.</li>
    <li><span class="list-heading">Winding Up Complexity:</span> LLP dissolves with less than two partners for six months or inability to pay debts.</li>
    <li><span class="list-heading">Limited Capital Raising:</span> No concept of equity or shareholders, making it difficult to attract investment from venture capitalists and angel investors.</li>
</ul><br>









                                    

                                    
                                    


<h4>Documents Required for LLP Registration:</h4><br>

<p>A. Documents of Partners:</p>
<ul>
    <li><span class="list-heading">PAN Card/ID Proof of Partners:</span> Primary identification proof for all partners.</li>
    <li><span class="list-heading">Address Proof of Partners:</span> Voter’s ID, Passport, Driver’s license, or Aadhar Card.</li>
    <li><span class="list-heading">Residence Proof of Partners:</span> Recent bank statement, telephone/mobile bill, electricity/gas bill.</li>
    <li><span class="list-heading">Photograph:</span> Passport size photograph of partners (preferably on a white background).</li>
    <li><span class="list-heading">Passport (for Foreign Nationals/NRIs):</span> Compulsory for foreign nationals/NRIs; notarized or apostilled.</li>
    <li><span class="list-heading">Address Proof (Foreign Nationals/NRIs):</span> Driving license, bank statement, residence card, or government-issued ID.</li>
</ul>
<p>If documents are not in English, provide notarized or apostilled translations.</p><br>

<p>B. Documents of LLP:</p>
<ul>
    <li><span class="list-heading">Proof of Registered Office Address:</span> Submit within 30 days of incorporation.</li>
    <li><span class="list-heading">For Rented Office:</span> Rent agreement and landlord's no-objection certificate.</li>
    <li><span class="list-heading">Utility Bill:</span> Gas, electricity, or telephone bill (not older than 2 months).</li>
    <li><span class="list-heading">Digital Signature Certificate:</span> One designated partner's digital signature for authorized signatory.</li>
</ul><br>

<h4>LLP Forms:</h4>
<ul>
    <li><span class="list-heading">FiLLiP:</span> Incorporation of LLP.</li>
    <li><span class="list-heading">Form 1:</span> Reserve LLP name.</li>
    <li><span class="list-heading">Form 2A:</span> Details of designated and other partners.</li>
    <li><span class="list-heading">Form 3:</span> Information about LLP agreement.</li>
    <li><span class="list-heading">Form 8:</span> Statement of Account and Solvency.</li>
    <li><span class="list-heading">Form 11:</span> Annual Return of LLP.</li>
    <li><span class="list-heading">Form 17:</span> Conversion of a firm into LLP.</li>
    <li><span class="list-heading">Form 18:</span> Conversion of a private/unlisted public company into LLP.</li>
    <li><span class="list-heading">Form 24:</span> Striking off name of LLP.</li>
</ul><br>

<h4>Checklist for LLP Registration:</h4><br>
<ul>
<li>Minimum of two partners.</li>
    <li>Digital Signature Certificate (DSC) for designated partners.</li>
    <li>Designated Partner Identification Number (DPIN) for designated partners.</li>
    <li>Unique LLP name.</li>
    <li>Capital contribution by partners.</li>
    <li>LLP Agreement between partners.</li>
    <li>Proof of registered office.</li>
</ul><br>


