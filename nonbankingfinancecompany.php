                                    <h4>Non Banking Finance Company REGISTRATION</h4><br>
                                    <ul class="attractive-list">
   
    <li><span class="list-heading">A Non–Banking Financial Corporation is a company incorporated under the Companies Act 2013 or 1956.</span> According to section 45-I (c) of the RBI Act, a Non–Banking Company carrying on the business of a financial institution will be an NBFC. It further states that the NBFC must be engaged in the business of Loans and Advances, Acquisition of stocks, equities, debt etc issued by the government or any local authority or other marketable securities.</li>
    <li><span class="list-heading">A Non-Banking Financial Corporation (NBFC)</span> A non-banking institution that is a company and has principal business of receiving deposits under any scheme or arrangement by any mode is also a non-banking financial company (Residuary non-banking company).</li>
    <li><span class="list-heading">Exclusions from the definition:</span> The NBFC business does not include business whose principal business is the following:</li>
    <ul>
        <li>Agricultural Activity</li>
        <li>Industrial Activity</li>
        <li>Purchase or sale of any goods excluding securities</li>
        <li>Sale/purchase/construction of any immovable property – Providing of any services</li>
    </ul>
    <li><span class="list-heading">Meaning of Principal Business:</span> The Reserve Bank of India has defined financial activity as principal business to bring clarity to the entities that will be monitored and regulated as NBFC under the RBI Act. The criteria s is called the 50-50 test and its as follows:</li>
    <ul>
        <li>The company’s financial assets must constitute 50% of the total assets.</li>
        <li>The income from financial assets must constitute 50% of the total income. It is governed by the Ministry of Corporate Affairs as well as the Reserve Bank of India. The License for operation is obtained from the RBI and it is incorporated as a company under applicable laws of the land.</li>
    </ul>
    <li><span class="list-heading">Different types of NBFCs</span></li>
    <li><span class="list-heading">NBFCs Which Need Not be Registered With RBI</span></li>
    <ul>
        <li>Core Investment Companies – (assets are less than 100 crore or public funds not taken)</li>
        <li>Merchant Banking Companies</li>
        <li>Companies that are engaged in the business of stock-broking</li>
        <li>Housing Finance Companies</li>
        <li>Companies engaged in the business of Venture Capital.</li>
        <li>Insurance companies holding a certificate of registration issued by IRDA.</li>
        <li>Chit Fund Companies as defined in the Sec 2 clause (b) of the Chit Fund Act, 1982</li>
        <li>Nidhi Companies as notified under Section 620(A) of the Companies Act 1956</li>
    </ul>
    <li><span class="list-heading">Procedure to Incorporate an NBFC</span></li>
    <ul>
        <li>A company should first be registered under the Companies Act 2013 or should already be registered under the Companies Act 1956 as either a Private Limited or a Public Limited Company.</li>
        <li>The minimum net owned funds of the Company should be Rs. 2 Crore.</li>
        <li>1/3rd of the Directors must possess finance experience.</li>
        <li>The CIBIL records of the Company should be clean.</li>
        <li>The company must have a detailed business plan for five years.</li>
        <li>The company must comply with the requirements for capital compliances and FEMA.</li>
        <li>After all of the above conditions have been satisfied the online application on the website of RBI should be filled and submitted along with the requisite documents.</li>
        <li>A CARN Number will be generated.</li>
        <li>A hard copy of the application also has to be sent to the regional branch of the Reserve Bank of India.</li>
        <li>After the application is properly scrutinized, the License will be given to the Company.</li>
    </ul>
    <li><span class="list-heading">Guidelines an NBFC Needs to Follow</span></li>
    <ul>
        <li>Once the Company gets a valid license it has to adhere to the following guidelines:</li>
        <li>They cannot receive deposits that are payable on demand.</li>
        <li>The public Deposits which the company can take should be for a minimum time period of 12 months and a maximum time period of 60 months.</li>
        <li>The interest charged by the Company cannot be more than the ceiling prescribed by the Reserve Bank of India from time to time.</li>
        <li>The repayment of any amount so taken by the Company will not be guaranteed by the Reserve Bank of India.</li>
        <li>All the information about the company as well as any change in the composition of the Company has to be furnished to the Reserve Bank of India.</li>
        <li>The deposits taken by the Public will be unsecured.</li>
        <li>The Company has to submit its audited balance sheet every year.</li>
        <li>A statutory return on the deposits taken by the company has to be furnished in the form NBS – 1 every year.</li>
        <li>A Quarterly Return on the liquid assets of the company has to be furnished.</li>
        <li>A certificate from the auditors had to be taken stating that the company is in a position to pay back all the deposits or money taken from the Public.</li>
        <li>A half-yearly Asset Liability Management (ALM) return has to be given by the company which has a Public Deposit of Rs. 20 Crore and above or has assets worth Rs. 100 Crore and above.</li>
        <li>The credit rating has to be taken every 6 months and submitted to the RBI.</li>
        <li>A minimum level of 15% of the Public Deposits has to be maintained by the Company in Liquid Assets.</li>
        <li>If the NBFC defaults in the payment of any amount taken, the consumer can go to the National Company Law Tribunal or the Consumer Forum to file a suit against the Company.</li>
    </ul>
