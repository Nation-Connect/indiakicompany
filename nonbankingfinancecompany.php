                                    <h4>Document Checklist for NBFC Registration:</h4><br>
                                    
                                    <ul>
        <li>Information about the management</li>
        <li>Certified copies of Certificate of Incorporation and Certificate of Commencement of Business in case of public limited companies.</li>
        <li>Certified copies of up-to-date Memorandum and Articles of Association of the company. Details of clauses in the memorandum relating to financial business.</li>
        <li>Copy of PAN/CIN allotted to the company.</li>
        <li>Directors’ profile, separately filled up and signed by each director.</li>
        <li>Certificate from the respective NBFC/s where the Directors have gained NBFC experience.</li>
        <li>CIBIL Data pertaining to Directors of the company</li>
        <li>Financial Statements of the last 2 years of Unincorporated Bodies, if any, in the group where the directors may be holding directorship with/without substantial interest.</li>
        <li>Board Resolution specifically approving the submission of the application and its contents and authorising signatory.</li>
        <li>Board Resolution to the effect that the company has not accepted any public deposit, in the past (specify period)/does not hold any public deposit as on the date and will not accept the same in the future without the prior approval of Reserve Bank of India in writing.</li>
        <li>Board resolution stating that the company is not carrying on any NBFC activity/stopped NBFC activity and will not carry on/commence the same before getting registration from RBI.</li>
        <li>Certified copy of Board Resolution for the formulation of “Fair Practices Code”.</li>
        <li>Statutory Auditors Certificate certifying that the company is/does not accept/is not holding Public Deposit.</li>
        <li>Statutory Auditors Certificate certifying that the company is not carrying on any NBFC activity.</li>
        <li>Statutory Auditors Certificate certifying net owned fund as on the date of the application.</li>
        <li>Details of Authorised Share Capital and the latest shareholding pattern of the company including the percentages.</li>
        <li>Copy of Fixed Deposit receipt & bankers certificate of no lien indicating balances in support of Net Owned Funds.</li>
        <li>Details of the bank balances/bank accounts/complete postal address of the branch/bank, loan/credit facilities etc. availed.</li>
        <li>Last three years Audited balance sheet and Profit & Loss account along with directors & auditors report or for such a shorter period as are available (for companies already in existence).</li>
        <li>Business plan of the company for the next three years giving details of its (a) thrust of business, (b) market segment, and (c) projected balance sheets, cash flow statement, asset/income pattern statement without any element of public deposits.</li>
        <li>Source of the startup capital of the company substantiated with documentary evidence.</li>
        <li>Self-attested Bank Statement/IT returns etc.</li>
    </ul>
