                                    <h4>Conditions to be fulfilled for getting ‘Nidhi’ status</h4><br>


                                    <div class="nidhi-conditions">
    <p>Nidhi Company should have a minimum of <strong>200 members</strong> within one year from commencement.</p>
    <p>The net owned funds should be <strong>10 lakh rupees or more</strong>. Net owned funds calculation: Equity share capital + free reserves (-) accumulated losses (-) intangible assets.</p>
    <p><strong>Unencumbered term deposits</strong> must be <strong>10% or higher</strong> of the outstanding deposits.</p>
    <p>The ratio of net owned funds to deposits <strong>shouldn't exceed 1:20</strong>.</p>
    <p>If the Nidhi Company meets all the above conditions, it must file <strong>NDH-1</strong> along with prescribed fees within <strong>90 days</strong> from the end of the first financial year after incorporation. The form should be certified by a practicing <strong>CA/CS/CWA</strong>.</p>
    <p>An <strong>extension</strong> of another financial year can be sought by submitting <strong>NDH-2</strong> to the Regional Director within <strong>30 days</strong> from the end of the first financial year.</p>
    <p>If the requirements are not met even after the second financial year, the company <strong>can't accept deposits</strong> until compliance is achieved, and <strong>penalties may be imposed</strong>.</p>
</div>





                                 
